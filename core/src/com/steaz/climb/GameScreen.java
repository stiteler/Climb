	public GameScreen(MyGame game) {
		this.game = game;
	}
	
	@Override
	public void render(float delta) {
		Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT);
		Gdx.gl.glClearColor(1, 1, 1, 1);
		
		spriteBatch.begin();
		spriteBatch.draw(new Texture(Gdx.files.internal("sprite-animation1.png")), 200, 500);
		spriteBatch.end();
	}

